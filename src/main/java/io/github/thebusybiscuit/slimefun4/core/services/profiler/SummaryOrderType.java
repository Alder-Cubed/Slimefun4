public enum SummaryOrderType {

    /**
     * Sort by highest to the lowest total timings
     */
    HIGHEST,
    /**
     * Sort by lowest to the highest total timings
     */
    LOWEST,
    /**
     * Sort by average timings (highest to lowest)
     */
    AVERAGE,
}
